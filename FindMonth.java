package function;

public class FindMonth {
	public static void main(String[]args) {
		String month="september";
		switch (month) {
		case "january":System.out.println("First month");break;
		case "february":System.out.println("second month");break;
		case "march":System.out.println("third month");break;
		case "april":System.out.println("fourth month");break;
		case "may":System.out.println("fifth month");break;
		case "june":System.out.println("sixth month");break;
		case "july":System.out.println("seventh month");break;
		case "august":System.out.println("eight month");break;
		case "september":System.out.println("ninth month");break;
		case"october":System.out.println("tenth month");break;
		case"november":System.out.println("eleventh month");break;
		case"december":System.out.println("twelth month");break;
		default :System.out.println("invalid");break;
	
		
		}
		
	}

}
