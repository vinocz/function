package function;

public class Rating {
	public static void main(String[]args) {
		int rating=5;
		if (rating==5) {
			System.out.println("excellent");
			}
		else if(rating==4) {
			System.out.println("good");
			
		}
		else if(rating==3) {
			System.out.println("average");
			
		}
		else if(rating==2) {
			System.out.println("not bad");
			
		}
		else {
			System.out.println("poor");
		}
		
	}

}
